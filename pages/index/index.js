Page({
  data: {
    tabOptions,
    textOptions,
    currentTab:'wet',
    currentView:'wet',
    viewHeights:{},
    showModal:false,
    showQr:false,
    currentGoods:{},
    qrCodePath:'/images/hx.jpg'
  },
  onLoad: function () {
    
  },
  onShow:function(){
    let that = this;
    this.setData({
      currentTab:'wet',
      currentView:'wet'
    },function(){
      that.getAllView();
    })
  },
  //分享
  onShareAppMessage: function (res) {
    
  },
  //切换tab
  handleTapTab(e) {
    let key = e.currentTarget.id;
    let { viewHeights } = this.data;
    this.setData({
      currentTab: key,
      currentView:key
    })
  },
  handleContact(e){
    console.log(e);
  },
  //获取view的高度
  getAllView: function () {
    const that = this;
    // 循环所有 view 项
    wx.createSelectorQuery().selectAll('.content-wrapper').boundingClientRect(rects => {
      let viewHeights = {};
      let init = 0;
      rects.forEach(function (rect) {
        let factH = rect.height - 40;
        viewHeights[rect.id] = init += factH; 
      })
      that.setData({
        viewHeights: viewHeights
      })
    }).exec()
  },
  //搜索
  handleSearch(){
    wx.navigateTo({
      url: '/pages/search/search'
    })
  },
  //滚动页面
  onScroll(e) {
    let that = this;
    let scrollTop = e.detail.scrollTop;
    let { currentTab,viewHeights } = this.data;
    if (scrollTop < viewHeights.wet){
      that.setData({
        currentTab:'wet'
      })
    } else if (scrollTop < viewHeights.dry){
      that.setData({
        currentTab: 'dry'
      })
    } else if (scrollTop < viewHeights.cycle){
      that.setData({
        currentTab: 'cycle'
      })
    }else{
      that.setData({
        currentTab: 'harmful'
      })
    }
  },
  //查看物品
  handleViewGood(e){
    let key = e.currentTarget.dataset.key;
    let goodName = e.currentTarget.dataset.name;
    let target = Object.assign({}, tabOptions[key],{
      goodName:goodName
    })
    this.setData({
      currentGoods: target,
      showModal:true
    })
  },
  handleToggle(e){
    this.setData({
      showModal: false
    })
  },
  //查看小程序码
  handleViewQrCode(e){
    this.setData({
      showQr:true
    })
  },
  handleCloseModal(){
    this.setData({
      showQr: false
    })
  },
  //保存图片到相册
  handleSaveQrCode(e){
    let filePath = e.currentTarget.dataset.qr;
    //打开设置
    // wx.openSetting({
    //   success(res) {
    //     console.log(res)
    //   }
    // })
    wx.getSetting({
      success: res => {
        console.log('授权信息：',res);
        if (res.authSetting['scope.writePhotosAlbum']) {
          wx.saveImageToPhotosAlbum({
            filePath: filePath,
            success(res) {
              wx.showToast({
                title: '保存成功！',
                icon:'none'
              })
            },
            fail: function (err) {
              console.log(err);
            }
          })
        }else{
          wx.authorize({
            scope: 'scope.writePhotosAlbum',
            success() {
              wx.saveImageToPhotosAlbum({
                filePath: filePath,
                success(res) {
                  wx.showToast({
                    title: '保存成功！',
                    icon: 'none'
                  })
                },
                fail: function (err) {
                  console.log(err);
                }
              })
            }
          })
        }
      }
    })
    console.log(filePath)
  }
})
